void*
_ccmaps(void* b, size_t n, int in, int out)
{
	register unsigned char*		s;
	register unsigned char*		e;
	register const unsigned char*	m;

	if (in != out)
	{
		m = CCMAP(in, out);
		for (e = (s = (unsigned char*)b) + n; s < e; s++)
			*s = m[*s];
	}
	return b;
}

